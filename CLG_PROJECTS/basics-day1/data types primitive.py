#data types - primivtive data types - int, float, str, bool - accepts only one value at a time
#non primitive data types - list, tuple, set, dict- can accept multiple values at a time

name = "Varsha"
age =25
height = 5.5
is_student = True
print(name,type(name))  # Output: Varsha <class 'str'>
print(age,type(age))   # Output: 25 <class 'int'>
print(height,type(height))  # Output: 5.5 <class 'float'>   
print(is_student,type(is_student))  # Output: True <class 'bool'>

